package ejercicios.series;

public class Series1 {  //12233344455555
    public static void main(String[] args) {
        int n = 7;
        for (int i = 0; i < n ; i++) {
            int c =0;
            while (c<=i){
                System.out.print(i+1);
                c++;
            }
        }
    }
}
